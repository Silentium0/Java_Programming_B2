package day31_custom_class;

public class Address {

    String street;
    String city;
    String state;
    int zip;



    public Address (String inputSreet, String inputCity, String inputState, int inzip){
        this.street = inputSreet;
        this.city = inputCity;
        this.state = inputState;
        this.zip = inzip;
    }


}

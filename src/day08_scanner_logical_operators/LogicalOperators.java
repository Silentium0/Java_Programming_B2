package day08_scanner_logical_operators;

public class LogicalOperators {

    public static void main(String[] args) {


        int  n =  20;

        System.out.println(n > 5);
        System.out.println(n < 10);

        System.out.println( n > 5 && n <10); // we  USE  here &&
        //                  true      false  ---->  false

        System.out.println( n >5 || n <10); // we use here ||

        System.out.println(!true); // we use ! flipped
        System.out.println(!false); // we use ! its just flipped






    }



}
